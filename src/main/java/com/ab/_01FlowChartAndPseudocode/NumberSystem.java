package com.ab._01FlowChartAndPseudocode;

public class NumberSystem {
    static void main() {
        int a  = 0b101;
        System.out.println("binary number is : " + a);

        int b =  012;
        System.out.println("octal number is : " + b);

        int c =  0xA;
        System.out.println("hexadecimal number is : " + c);
    }
}
