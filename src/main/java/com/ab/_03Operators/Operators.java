package com.ab._03Operators;

public class Operators {
    static void main() {

        int a = 10;
        int b = 20;
        int c = 30;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("b / a = " + (b / a));
        System.out.println("b % a = " + (b % a));

        System.out.println("c++ = " + (c++));
        System.out.println("++c = " + (++c));
        System.out.println("c-- = " + (c--));
        System.out.println("--c = " + (--c));


    }
}
